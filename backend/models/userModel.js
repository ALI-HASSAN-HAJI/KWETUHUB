import {model, Schema} from 'mongoose';

const userSchema = new Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String, 
    required: true
  },
  gender: { 
    type: String,
    // required: true
  },
  dateOfBirth: {
    type: Date,
    required: true
  },
  countryCode: {
    type: String,
    // required: true
  },
  cart: [Object],
});

export default model("userModel", userSchema); 